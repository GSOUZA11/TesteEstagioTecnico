//A diferença entre uma classe abstrata e uma interface é que uma classe abstrata pode conter implementação de métodos, enquanto uma interface não pode. Além disso, uma classe pode herdar de apenas uma classe abstrata, mas pode implementar várias interfaces.

//Uma classe abstrata é uma classe que não pode ser instanciada, ou seja, não pode ser criada um objeto a partir dela. 
//Ela é usada para definir um conjunto de métodos e propriedades que devem ser implementados por classes derivadas. 
//Uma classe abstrata pode conter métodos abstratos, que são métodos sem implementação, e métodos concretos, que são métodos com implementação.

//Uma interface é uma estrutura que define um conjunto de métodos e propriedades que devem ser implementados por uma classe. Uma interface não pode conter implementação de métodos, apenas a assinatura dos métodos. 

//Uma classe pode implementar várias interfaces, o que significa que ela pode herdar de várias classes abstratas e implementar várias interfaces.